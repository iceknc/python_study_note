import re


def main():
    # 校验密码强度  必须是包含大小写字母和数字的组合，不能使用特殊字符，长度在8-10之间
    re.compile("^(?=.*\\d)(?=.*[a-z])(?=.*[A-Z]).{8,10}$")

    # 校验中文  字符串仅能是中文
    re.compile("^[\\\u4e00-\\\u9fa5]{0,}$")

    # 由数字、26个英文字母或下划线组成的字符串
    re.compile("^\\w+$")

    # 校验E-Mail 地址
    re.compile(
        "[\\w!#$%&'*+/=?^_`{|}~-]+(?:\\.[\\w!#$%&'*+/=?^_`{|}~-]+)*@(?:[\\w](?:[\\w-]*[\\w])?\\.)+[\\w](?:[\\w-]*[\\w])?")

    # 校验身份证号码
    re.compile("^[1-9]\\d{7}((0\\d)|(1[0-2]))(([0|1|2]\\d)|3[0-1])\\d{3}$")  # 15位
    re.compile("^[1-9]\\d{5}[1-9]\\d{3}((0\\d)|(1[0-2]))(([0|1|2]\\d)|3[0-1])\\d{3}([0-9]|X)$")  # 18位

    # 校验日期  yyyy-mm-dd格式的日期校验，已考虑平闰年
    re.compile("^(?:(?!0000)[0-9]{4}-(?:(?:0[1-9]|1[0-2])-(?:0[1-9]|1[0-9]|2[0-8])|(?:0[13-9]|1[0-2])-(?:29|30)|"  #
               "(?:0[13578]|1[02])-31)|(?:[0-9]{2}(?:0[48]|[2468][048]|[13579][26])|(?:0[48]|[2468][048]|"  #
               "[13579][26])00)-02-29)$")

    # 校验金额  精确到2位小数
    re.compile("^[0-9]+(.[0-9]{2})?$")

    # 校验手机号  可根据目前国内收集号扩展前两位开头号码
    re.compile("^(13[0-9]|14[5|7]|15[0|1|2|3|5|6|7|8|9]|18[0|1|2|3|5|6|7|8|9])\\d{8}$")

    # 判断IE的版本
    re.compile("^.*MSIE [5-8](?:\\.[0-9]+)?(?!.*Trident\\/[5-9]\\.0).*$")

    # 校验IP-v4地址
    re.compile("\\b(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\b")

    # 校验IP-v6地址
    re.compile("(([0-9a-fA-F]{1,4}:){7,7}[0-9a-fA-F]{1,4}|([0-9a-fA-F]{1,4}:){1,7}:|([0-9a-fA-F]{1,4}:){1,6}:"  #
               "[0-9a-fA-F]{1,4}|([0-9a-fA-F]{1,4}:){1,5}(:[0-9a-fA-F]{1,4}){1,2}|([0-9a-fA-F]{1,4}:){1,4}(:"  #
               "[0-9a-fA-F]{1,4}){1,3}|([0-9a-fA-F]{1,4}:){1,3}(:[0-9a-fA-F]{1,4}){1,4}|([0-9a-fA-F]{1,4}:){1,2}"  #
               "(:[0-9a-fA-F]{1,4}){1,5}|[0-9a-fA-F]{1,4}:((:[0-9a-fA-F]{1,4}){1,6})|:((:[0-9a-fA-F]{1,4}){1,7}|:"  #
               ")|fe80:(:[0-9a-fA-F]{0,4}){0,4}%[0-9a-zA-Z]{1,}|::(ffff(:0{1,4}){0,1}:){0,1}((25[0-5]|(2[0-4]|1{0,1}"  #
               "[0-9]){0,1}[0-9])\\.){3,3}(25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])|([0-9a-fA-F]{1,4}:){1,4}:"  #
               "((25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])\\.){3,3}(25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9]))")

    # 检查URL的前缀
    re.compile("^[a-zA-Z]+:\\/\\//")

    # 提取URL链接
    re.compile("^(f|ht){1}(tp|tps):\\/\\/([\\w-]+\\.)+[\\w-]+(\\/[\\w- ./?%&=]*)?")

    #  提取Color Hex Codes
    re.compile("^#([A-Fa-f0-9]{6}|[A-Fa-f0-9]{3})$")


if __name__ == "__main__":
    main()
